"""
RAG Retriever
=============

Retrieval tool for Xiangqi knowledge base.
Provides a unified interface for querying vector stores.

Supported Backends:
  Phase 1: In-memory mock (no dependencies)
  Phase 2: ChromaDB (embedded, zero-config)
  Phase 3: Pinecone / Weaviate (hosted, scalable)

Collections:
  - openings
  - tactics
  - endgames
  - beginner_principles

Each document has:
  - content: str         (the text)
  - metadata: dict       (topic, difficulty, piece_types, game_phase, etc.)
  - embedding: list[float]  (vector representation, generated by backend)
"""

from __future__ import annotations

import logging
from typing import Any, Optional

logger = logging.getLogger("tools.rag_retriever")

# Module-level singleton for embedding model (shared with knowledge_loader)
_embedding_model = None
_DEFAULT_MODEL_NAME = "BAAI/bge-m3"
_SIMILARITY_THRESHOLD = 0.3  # Cosine distance threshold (lower = more similar)

COLLECTION_NAMES = ["openings", "tactics", "endgames", "beginner_principles"]


def _get_embedding_model(model_name: str = _DEFAULT_MODEL_NAME):
    """Lazy-load and cache the sentence-transformer embedding model."""
    global _embedding_model
    if _embedding_model is None:
        try:
            from sentence_transformers import SentenceTransformer
            logger.info(f"Loading embedding model: {model_name} ...")
            _embedding_model = SentenceTransformer(model_name)
            logger.info("Embedding model loaded.")
        except ImportError:
            logger.error(
                "sentence-transformers not installed. "
                "Run: pip install sentence-transformers"
            )
            return None
    return _embedding_model


# ========================
#    RETRIEVER INTERFACE
# ========================

class RAGRetriever:
    """Pluggable RAG retrieval interface.

    Phase 1: Returns mock results from a built-in knowledge stub.
    Phase 2: Wraps ChromaDB for real vector similarity search.

    Usage:
        retriever = RAGRetriever(backend="mock")
        results = await retriever.retrieve("cannon tactics", collection="tactics")
    """

    def __init__(
        self,
        backend: str = "mock",
        chroma_path: Optional[str] = None,
        embedding_model: Optional[str] = None,
        similarity_threshold: Optional[float] = None,
    ):
        self._backend = backend
        self._chroma_path = chroma_path or "./chroma_db"
        self._embedding_model = embedding_model or _DEFAULT_MODEL_NAME
        self._similarity_threshold = (
            similarity_threshold if similarity_threshold is not None
            else _SIMILARITY_THRESHOLD
        )
        self._client: Any = None  # ChromaDB client or other backend

    async def initialize(self) -> bool:
        """Initialize the retriever backend.

        Returns True if backend is ready.
        """
        if self._backend == "chromadb":
            return await self._init_chromadb()
        elif self._backend == "mock":
            logger.info("RAG retriever initialized with mock backend")
            return True
        else:
            logger.warning(f"Unknown backend: {self._backend}, falling back to mock")
            self._backend = "mock"
            return True

    async def _init_chromadb(self) -> bool:
        """Initialize ChromaDB backend."""
        try:
            import chromadb
            self._client = chromadb.PersistentClient(path=self._chroma_path)
            logger.info(f"ChromaDB initialized at {self._chroma_path}")
            return True
        except ImportError:
            logger.error("chromadb not installed. Run: pip install chromadb")
            self._backend = "mock"
            return False
        except Exception as e:
            logger.error(f"ChromaDB initialization failed: {e}")
            self._backend = "mock"
            return False

    # ---- Retrieval ----

    async def retrieve(
        self,
        query: str,
        collection: str = "all",
        top_k: int = 3,
        filters: Optional[dict] = None,
    ) -> list[dict]:
        """Retrieve relevant documents for a query.

        Args:
            query: Natural language or keyword query.
            collection: Target collection name.
            top_k: Maximum number of results.
            filters: Metadata filters (e.g., {"difficulty": "beginner"}).

        Returns:
            List of dicts with "content", "score", and "metadata" keys.
        """
        if self._backend == "chromadb" and self._client:
            return await self._retrieve_chromadb(query, collection, top_k, filters)
        else:
            return self._retrieve_mock(query, collection, top_k, filters)

    def encode_query(self, text: str) -> Optional[list[float]]:
        """Encode a query string into a normalized embedding vector.

        Returns None if the embedding model is unavailable.
        """
        model = _get_embedding_model(self._embedding_model)
        if model is None:
            return None
        embedding = model.encode(text, normalize_embeddings=True)
        return embedding.tolist()

    async def _retrieve_chromadb(
        self,
        query: str,
        collection: str,
        top_k: int,
        filters: Optional[dict],
    ) -> list[dict]:
        """Retrieve from ChromaDB using vector embeddings."""
        try:
            # Support "all" collection — query each collection and merge
            collections_to_query = (
                COLLECTION_NAMES if collection == "all"
                else [collection]
            )

            query_embedding = self.encode_query(query)
            all_documents = []

            for coll_name in collections_to_query:
                try:
                    coll = self._client.get_collection(name=coll_name)
                except Exception:
                    logger.debug(f"Collection '{coll_name}' not found, skipping")
                    continue

                if coll.count() == 0:
                    continue

                where_filter = filters if filters else None

                # Use vector search if embedding available, else fall back
                if query_embedding is not None:
                    results = coll.query(
                        query_embeddings=[query_embedding],
                        n_results=min(top_k, coll.count()),
                        where=where_filter,
                    )
                else:
                    results = coll.query(
                        query_texts=[query],
                        n_results=min(top_k, coll.count()),
                        where=where_filter,
                    )

                if results and results["documents"]:
                    for i, doc in enumerate(results["documents"][0]):
                        distance = (
                            results["distances"][0][i]
                            if results.get("distances")
                            else 0.5
                        )
                        # Cosine distance: 0 = identical, 2 = opposite
                        # Convert to similarity score: 1 - distance
                        score = 1.0 - distance
                        meta = (
                            results["metadatas"][0][i]
                            if results.get("metadatas")
                            else {}
                        )
                        meta["_collection"] = coll_name
                        all_documents.append({
                            "content": doc,
                            "score": score,
                            "metadata": meta,
                        })

            # Filter by similarity threshold
            all_documents = [
                d for d in all_documents
                if d["score"] >= (1.0 - self._similarity_threshold)
            ]

            # Sort by score descending, return top_k
            all_documents.sort(key=lambda d: d["score"], reverse=True)
            return all_documents[:top_k]

        except Exception as e:
            logger.error(f"ChromaDB query failed: {e}")
            return self._retrieve_mock(query, collection, top_k, filters)

