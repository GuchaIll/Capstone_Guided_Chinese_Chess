    # ---- Collection Management ----

    async def add_documents(
        self,
        collection: str,
        documents: list[dict],
    ) -> bool:
        """Add documents to a collection (ChromaDB backend only).

        Each document dict should have:
            - "content": str
            - "metadata": dict (optional)
            - "id": str (optional, auto-generated if missing)

        Returns True on success.
        """
        if self._backend != "chromadb" or not self._client:
            logger.warning("add_documents only supported with ChromaDB backend")
            return False

        try:
            coll = self._client.get_or_create_collection(name=collection)
            ids = [
                d.get("id", f"{collection}_{i}")
                for i, d in enumerate(documents)
            ]
            contents = [d["content"] for d in documents]
            metadatas = [d.get("metadata", {}) for d in documents]

            coll.add(documents=contents, ids=ids, metadatas=metadatas)
            logger.info(
                f"Added {len(documents)} documents to '{collection}'"
            )
            return True
        except Exception as e:
            logger.error(f"Failed to add documents: {e}")
            return False

    async def list_collections(self) -> list[str]:
        """List available collection names."""
        if self._backend == "chromadb" and self._client:
            try:
                collections = self._client.list_collections()
                return [c.name for c in collections]
            except Exception as e:
                logger.error(f"Failed to list collections: {e}")
        return list(mock_db.keys()) if self._backend == "mock" else []
